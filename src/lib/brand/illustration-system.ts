const ALL_ILLUSTRATIONS: IllustrationSpec[] = [
  ...EMPTY_STATE_ILLUSTRATIONS,
  ...ONBOARDING_ILLUSTRATIONS,
  ...CELEBRATION_ILLUSTRATIONS,
  ...ERROR_ILLUSTRATIONS,
  ...FESTIVAL_ILLUSTRATIONS,
];

export function getIllustrationAltText(name: string, locale: IllustrationLocale = 'en'): string {
  const spec = ALL_ILLUSTRATIONS.find((s) => s.name === name);
  if (!spec) return '';
  return spec.altTexts[locale] ?? spec.altTexts.en;
}

export function getIllustrationPath(name: string, variant: 'light' | 'dark' | 'auto' = 'auto'): string {
  const folder = variant === 'auto' ? '' : `${variant}/`;
  return `/illustrations/${folder}${name}.svg`;
}
